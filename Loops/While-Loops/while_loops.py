# Loops is a way to repeat a block of code, it repeats until a condition is met
# There are two types of loops, for loops and while loops

# While loops are used when you don't know how many times you want to repeat a block of code
# For loops are used when you know how many times you want to repeat a block of code

# we dont know this number, so we use a while loop
num = -51

while num < 50:
    print(num)
    # We need to increment num by 1, otherwise the loop will never end, The condition will always be true
    num += 1

